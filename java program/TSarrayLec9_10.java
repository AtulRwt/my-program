import java.util.*;
public class TSarrayLec9_10{
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int n=sc.nextInt();
        int num[]=new int [n];
        for(int i=0;i<n;i++){
            num[i]=sc.nextInt();
        }
        for(int i=0;i<5;i++)
        {
            System.out.println(num[i]);
        }
    }
}